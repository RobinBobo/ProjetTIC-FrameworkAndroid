package model;

import java.util.ArrayList;
import java.util.List;


public class Magasin {
	
	private Catalogue c;
	private List<Client>  myCustomers;

	public Magasin() {		
		c = new Catalogue ();
		myCustomers = new ArrayList <Client> ();
	}	

	public Catalogue getCatalogue () {
		return this.c;
	}
	
	public void setCatalogue (Catalogue cat) {
		this.c=cat;
	}
	
	public void addCustomer (Client cl) {
		this.myCustomers.add(cl);
	}


}